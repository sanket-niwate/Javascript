var arr=[1,2,3,4,5];    //array content string also in js
console.log(arr);

arr.push(6)           //push mean add 
console.log(arr);

arr.pop()           //pop mean remove 
console.log(arr);

arr.shift()
console.log(arr);  //shift mean remove from starting

arr.unshift(1)
console.log(arr); //unshift mean add from starting


arr[0]='sanket';   // change the element
console.log(arr);


